#include <string.h>

char	*ft_toupperstring(char *c)
{
	char *temp;

	temp = c;
	if (c)
	{
		while (*c)
		{
			if (*c > 96 && *c < 123)
				*c = *c - 32;
			c++;
		}
		return (temp);
	}
	else
		return (NULL);
}
